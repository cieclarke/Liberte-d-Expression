    <h2>Product design</h2>
    <div class="description">
        <p><b>Concept rational 2:</b> Whilst reflecting the Goring Hotel's history and classical style, Tim Gosling's approach to the interior design of the lounge room draws some of its inspiration from the art deco references visible throughout the space. Using the Art Deco elements as the starting point of the design, the research undertaken indicated that the main characteristics of the style are clean lines and sharp edges, stylishness and symmetry. It's sleek, streamlined forms conveys elegance and sophistication, which is very much in line with the qualities of the Cheney's best sellers today. However, the use of Art Deco features will not be immediately obvious or overwhelming in order to appeal to a wide audience and ensure its commercial viability. With this in mind, the design of the new chimney piece will draw inspiration from the identity, history and look of the hotel, in this instance the classical and art deco influence of the interior.</p>
