<h1>Interior Design</h1>

<p>A few years ago, we took a change of direction setting up an art consultancy business. Whilst working on a number of residential and commercial art procurement projects, we then moved on into interior and furniture design.</p>

<section class="interior">
    <h2>Interior design</h2>
    <div class="description">
        <p><b>Concept rational 1:</b> For this project, I was inspired by one of Heston Blumentha’s main interests – rediscovering historical British gastronomy from Victorian, Tudor England to medieval times and ancient Rome. The idea came up whilst reading a number of articles and interviews about Heston Blumenthal. A particular sentence caught my attention <i>"... research into historic British gastronomy in a bid to recreate dishes inspired by the past."</i> I immediately thought this could be applied for the interior of the restaurant <i>"... research into historic British <b>styles</b> in a bid to recreate <b>interiors</b> inspired by the past."</i>  As the premises were a former pub circa 1550, it made sense to me to focus on the Tudor era and reinvent the interior style seen during this ear. The concept would look to take very traditional British design ideas and produce contemporary versions of them.</p>
